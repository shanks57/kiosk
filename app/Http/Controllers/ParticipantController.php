    /**
     * Update a participant record (inline edit from modal)
     */
    public function updateParticipant(Request $request, Event $event, Participant $participant)
    {
        $user = Auth::user();
        $organizer = $user->organizer;

        // Verify organizer owns this event
        if ($event->organizer_id != $organizer->id) {
            return response()->json(['error' => 'Unauthorized'], 403);
        }

        // Verify participant belongs to this event
        if ($participant->event_id != $event->id) {
            return response()->json(['error' => 'Participant does not belong to this event'], 404);
        }

        $validated = $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|email|unique:users,email,' . ($participant->user_id ?? 'NULL') . ',id',
            'phone' => 'nullable|string|max:20',
            'seat_id' => 'nullable|exists:event_seats,id',
        ]);

        // Update related user record (participant user)
        $userRecord = $participant->user;
        if ($userRecord) {
            $userRecord->name = $validated['name'];
            $userRecord->email = $validated['email'];
            $userRecord->phone = $validated['phone'] ?? $userRecord->phone;
            $userRecord->save();
        }

        // Update participant seat if provided
        if (array_key_exists('seat_id', $validated)) {
            $participant->seat_id = $validated['seat_id'];
        }

        $participant->save();

        return response()->json([
            'success' => true,
            'message' => 'Participant updated successfully',
            'data' => $participant->load('user', 'seat'),
        ]);
    }
