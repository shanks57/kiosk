        $user = Auth::user();
        $organizer = $user->organizer;

        abort_if($event->organizer_id != $organizer?->id, 403);

        $participants = Order::where('event_id', $event->id)
            ->with(['user.company', 'items.seat', 'items.category', 'items.company'])
            ->paginate(20)
            ->withQueryString();

