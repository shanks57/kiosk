    /**
     * Export participants list.
     */
    public function export(Event $event)
    {
        $user = Auth::user();
        $organizer = $user->organizer;

        $participants = OrderItem::whereHas('order', function ($q) use ($event) {
            $q->where('event_id', $event->id);
        })
            ->with(['order.user', 'seat', 'category'])
            ->get();

        // Generate CSV
        $csv = "Name,Email,Phone,Ticket Category,Seat,Order Date\n";
        foreach ($participants as $participant) {
            $csv .= sprintf(
                '"%s","%s","%s","%s","%s","%s"' . "\n",
                $participant->order->user->name ?? 'N/A',
                $participant->order->user->email ?? 'N/A',
                'N/A',
                $participant->category->name ?? 'N/A',
                $participant->seat ? sprintf('%s-%s', $participant->seat->row_number, $participant->seat->seat_number) : 'N/A',
                $participant->created_at->format('Y-m-d H:i')
            );
        }

        return response($csv)
            ->header('Content-Type', 'text/csv')
            ->header('Content-Disposition', 'attachment; filename="participants-' . $event->id . '.csv"');
    }

    /**
     * Store a new participant from an order item (via modal form).
     */
    public function storeFromOrderItem(Request $request, Event $event, OrderItem $orderItem)
    {
        $validated = $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|email',
            'phone' => 'nullable|string|max:20',
            'seat' => 'nullable|string|max:50',
            'user_id' => 'required|exists:users,id',
            'event_id' => 'required|exists:events,id',
        ]);

        $user = Auth::user();
        $organizer = $user->organizer;

        // Verify organizer owns this event
        if ($event->organizer_id != $organizer->id) {
            return response()->json(['error' => 'Unauthorized'], 403);
        }

        // Find or create user by email for the participant
        $participant = User::firstOrCreate(
            ['email' => $validated['email']],
            [
                'name' => $validated['name'],
                'phone' => $validated['phone'] ?? null,
                'password' => Hash::make(Str::random(16)),
                'email_verified_at' => now(),
            ]
        );

        // Update phone if user already existed and phone is provided
        if (!empty($validated['phone']) && empty($participant->phone)) {
            $participant->phone = $validated['phone'];
            $participant->save();
        }

        // Create participant record
        $participantRecord = $orderItem->participant()->create([
            'event_id' => $validated['event_id'],
            'event_date' => $event->start_date ?? now(),
            'user_id' => $participant->id,
            'seat_id' => null, // Seat reference can be added if you have seat management
            'company_id' => $participant->company_id,
        ]);

        return response()->json([
            'success' => true,
            'message' => 'Participant added successfully',
            'data' => $participantRecord,
        ], 201);
    }

